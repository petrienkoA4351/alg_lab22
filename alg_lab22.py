class JPEGCompressor:
    """Класс для JPEG-подобного сжатия"""

    def __init__(self):
        self.huffman_tables = {}
        self.quant_tables = {}

    # 2.1 Resizing с билинейной интерполяцией
    @staticmethod
    def resize_bilinear(image_array: np.ndarray, new_size: Tuple[int, int]) -> np.ndarray:
        """Изменение размера изображения с использованием билинейной интерполяции"""
        old_h, old_w = image_array.shape[:2]
        new_h, new_w = new_size

        if len(image_array.shape) == 3:
            channels = image_array.shape[2]
            result = np.zeros((new_h, new_w, channels))

            for c in range(channels):
                result[:, :, c] = JPEGCompressor._resize_channel(
                    image_array[:, :, c], new_h, new_w, old_h, old_w
                )
        else:
            result = JPEGCompressor._resize_channel(image_array, new_h, new_w, old_h, old_w)

        return result.astype(image_array.dtype)

    @staticmethod
    def _resize_channel(channel: np.ndarray, new_h: int, new_w: int, old_h: int, old_w: int) -> np.ndarray:
        """Изменение размера для одного канала"""
        result = np.zeros((new_h, new_w))

        # Коэффициенты масштабирования
        y_scale = (old_h - 1) / (new_h - 1) if new_h > 1 else 0
        x_scale = (old_w - 1) / (new_w - 1) if new_w > 1 else 0

        for y in range(new_h):
            for x in range(new_w):
                # Координаты в исходном изображении
                old_y = y * y_scale
                old_x = x * x_scale

                y1 = int(np.floor(old_y))
                y2 = min(y1 + 1, old_h - 1)
                x1 = int(np.floor(old_x))
                x2 = min(x1 + 1, old_w - 1)

                # Веса для интерполяции
                wy = old_y - y1
                wx = old_x - x1

                # Билинейная интерполяция
                val = (1 - wy) * (1 - wx) * channel[y1, x1] + \
                      (1 - wy) * wx * channel[y1, x2] + \
                      wy * (1 - wx) * channel[y2, x1] + \
                      wy * wx * channel[y2, x2]

                result[y, x] = val

        return result

    # 2.2 Зигзаг обход
    @staticmethod
    def zigzag_traversal(matrix: np.ndarray) -> List[int]:
        """Зигзаг обход матрицы"""
        h, w = matrix.shape
        result = []

        for s in range(h + w - 1):
            if s % 2 == 0:  # Четная диагональ - вверх
                for i in range(min(s, h-1), max(0, s-w+1)-1, -1):
                    j = s - i
                    if 0 <= i < h and 0 <= j < w:
                        result.append(matrix[i, j])
            else:  # Нечетная диагональ - вниз
                for i in range(max(0, s-w+1), min(s, h-1)+1):
                    j = s - i
                    if 0 <= i < h and 0 <= j < w:
                        result.append(matrix[i, j])

        return result

    @staticmethod
    def inverse_zigzag_traversal(values: List[int], shape: Tuple[int, int]) -> np.ndarray:
        """Обратный зигзаг обход для восстановления матрицы"""
        h, w = shape
        matrix = np.zeros((h, w))
        idx = 0

        for s in range(h + w - 1):
            if s % 2 == 0:
                for i in range(min(s, h-1), max(0, s-w+1)-1, -1):
                    j = s - i
                    if 0 <= i < h and 0 <= j < w and idx < len(values):
                        matrix[i, j] = values[idx]
                        idx += 1
            else:
                for i in range(max(0, s-w+1), min(s, h-1)+1):
                    j = s - i
                    if 0 <= i < h and 0 <= j < w and idx < len(values):
                        matrix[i, j] = values[idx]
                        idx += 1

        return matrix

    # 2.3 Разностное кодирование и RLE
    @staticmethod
    def differential_encode(dc_coeffs: List[int]) -> List[int]:
        """Разностное кодирование DC коэффициентов"""
        encoded = [dc_coeffs[0]]
        for i in range(1, len(dc_coeffs)):
            encoded.append(dc_coeffs[i] - dc_coeffs[i-1])
        return encoded

    @staticmethod
    def differential_decode(encoded: List[int]) -> List[int]:
        """Декодирование DC коэффициентов"""
        decoded = [encoded[0]]
        for i in range(1, len(encoded)):
            decoded.append(decoded[-1] + encoded[i])
        return decoded

    @staticmethod
    def rle_encode(ac_coeffs: List[int]) -> List[Tuple[int, int]]:
        """RLE кодирование AC коэффициентов (стандарт ITU-T81)"""
        result = []
        zeros = 0

        for coeff in ac_coeffs:
            if coeff == 0:
                zeros += 1
            else:
                # Пропускаем более 15 нулей
                while zeros > 15:
                    result.append((15, 0))
                    zeros -= 15
                result.append((zeros, int(coeff)))
                zeros = 0

        # Конец блока
        if zeros > 0:
            result.append((0, 0))

        return result

    @staticmethod
    def rle_decode(rle_codes: List[Tuple[int, int]]) -> List[int]:
        """Декодирование RLE"""
        result = []

        for zeros, value in rle_codes:
            if zeros == 0 and value == 0:  # EOB
                # Добавляем оставшиеся нули до 63
                while len(result) < 63:
                    result.append(0)
                break

            # Добавляем нули
            result.extend([0] * zeros)
            result.append(value)

        # Дополняем нулями если нужно
        while len(result) < 63:
            result.append(0)

        return result[:63]

    # 2.4 Кодирование переменной длины
    @staticmethod
    def get_category(value: int) -> int:
        """Определение категории для кодирования"""
        abs_val = abs(value)
        if abs_val == 0:
            return 0
        return abs_val.bit_length()

    @staticmethod
    def get_binary_value(value: int, category: int) -> str:
        """Получение бинарного представления значения"""
        if value < 0:
            # Отрицательные числа: бинарное представление с дополнением
            return format((1 << category) + value, f'0{category}b')
        else:
            return format(value, f'0{category}b')

    # 2.5 Таблицы Хаффмана (стандартные из ITU-T81)
    @staticmethod
    def get_standard_dc_luminance_table() -> Dict[int, str]:
        """Стандартная таблица Хаффмана для DC яркости"""
        codes = {
            0: "00",
            1: "010",
            2: "011",
            3: "100",
            4: "101",
            5: "110",
            6: "1110",
            7: "11110",
            8: "111110",
            9: "1111110",
            10: "11111110",
            11: "111111110"
        }
        return codes

    @staticmethod
    def get_standard_ac_luminance_table() -> Dict[Tuple[int, int], str]:
        """Стандартная таблица Хаффмана для AC яркости"""
        # Упрощенная таблица для демонстрации
        codes = {
            (0, 0): "1010",  # EOB
            (0, 1): "00",
            (1, 1): "1100",
            (2, 1): "11011",
            # ... полная таблица была бы больше
        }
        return codes

    # 2.6 Адаптация таблицы квантования под качество
    @staticmethod
    def adjust_quantization_matrix(base_matrix: np.ndarray, quality: int) -> np.ndarray:
        """Адаптация матрицы квантования под заданное качество"""
        if quality < 1:
            quality = 1
        if quality > 100:
            quality = 100

        if quality < 50:
            scale = 5000 / quality
        else:
            scale = 200 - 2 * quality

        adjusted = np.ceil((base_matrix * scale) / 100)
        return np.clip(adjusted, 1, 255).astype(np.int32)


