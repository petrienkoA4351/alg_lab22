    # Конвертация в RAW формат
    def to_raw_format(self, image: Image.Image, color_space: str = 'RGB') -> bytes:
        """Конвертация изображения в собственный RAW формат"""
        width, height = image.size
        mode = image.mode

        if mode == 'RGB':
            data = np.array(image).tobytes()
            pixel_size = 3
        elif mode == 'L':
            data = np.array(image).tobytes()
            pixel_size = 1
        elif mode == '1':
            # Конвертируем битовое изображение в байты
            bw_data = np.array(image, dtype=np.uint8) * 255
            data = bw_data.tobytes()
            pixel_size = 1
        else:
            raise ValueError(f"Unsupported mode: {mode}")

        # Метаданные: тип(1 байт), ширина(4 байта), высота(4 байта), цветовое пространство(1 байт)
        img_type = 0 if mode == '1' else (1 if mode == 'L' else 2)
        color_space_code = {'RGB': 0, 'YCbCr': 1, 'GRAY': 2, 'BW': 3}.get(color_space, 0)

        metadata = struct.pack('!BIIB', img_type, width, height, color_space_code)

        return metadata + data

    def from_raw_format(self, raw_data: bytes) -> Image.Image:
        """Восстановление изображения из RAW формата"""
        img_type, width, height, color_space = struct.unpack('!BIIB', raw_data[:14])
        pixel_data = raw_data[14:]

        expected_size = width * height * (3 if img_type == 2 else 1)

        if len(pixel_data) != expected_size:
            raise ValueError(f"Data size mismatch. Expected {expected_size}, got {len(pixel_data)}")

        if img_type == 2:  # Цветное
            array = np.frombuffer(pixel_data, dtype=np.uint8).reshape(height, width, 3)
            return Image.fromarray(array, 'RGB')
        elif img_type == 1:  # Оттенки серого
            array = np.frombuffer(pixel_data, dtype=np.uint8).reshape(height, width)
            return Image.fromarray(array, 'L')
        else:  # Черно-белое
            array = np.frombuffer(pixel_data, dtype=np.uint8).reshape(height, width)
            return Image.fromarray(array > 127, '1')

    # 1.2 Цветовые пространства
    @staticmethod
    def rgb_to_ycbcr(rgb_array: np.ndarray) -> np.ndarray:
        """Конвертация RGB в YCbCr"""
        # Формулы из стандарта JPEG
        r = rgb_array[..., 0].astype(np.float32)
        g = rgb_array[..., 1].astype(np.float32)
        b = rgb_array[..., 2].astype(np.float32)

        y = 0.299 * r + 0.587 * g + 0.114 * b
        cb = 128 - 0.168736 * r - 0.331264 * g + 0.5 * b
        cr = 128 + 0.5 * r - 0.418688 * g - 0.081312 * b

        ycbcr = np.stack([y, cb, cr], axis=-1)
        return np.clip(ycbcr, 0, 255).astype(np.uint8)

    @staticmethod
    def ycbcr_to_rgb(ycbcr_array: np.ndarray) -> np.ndarray:
        """Конвертация YCbCr в RGB"""
        y = ycbcr_array[..., 0].astype(np.float32) - 128
        cb = ycbcr_array[..., 1].astype(np.float32) - 128
        cr = ycbcr_array[..., 2].astype(np.float32) - 128

        r = y + 1.402 * cr
        g = y - 0.344136 * cb - 0.714136 * cr
        b = y + 1.772 * cb

        rgb = np.stack([r, g, b], axis=-1)
        return np.clip(rgb, 0, 255).astype(np.uint8)

    # 1.3 Downsampling/Upsampling/Resizing
    @staticmethod
    def downsample_by_2(image_array: np.ndarray) -> np.ndarray:
        """Децимация изображения с коэффициентом 2"""
        height, width = image_array.shape[:2]
        return image_array[::2, ::2]

    @staticmethod
    def upsample_by_2(image_array: np.ndarray, original_shape: Tuple[int, int]) -> np.ndarray:
        """Апсемплинг изображения (повторение пикселей)"""
        upsampled = np.repeat(np.repeat(image_array, 2, axis=0), 2, axis=1)

        # Обрезаем до исходного размера если нужно
        if upsampled.shape[0] > original_shape[0]:
            upsampled = upsampled[:original_shape[0]]
        if upsampled.shape[1] > original_shape[1]:
            upsampled = upsampled[:, :original_shape[1]]

        return upsampled

    # Линейная интерполяция
    @staticmethod
    def linear_interpolation(x1: float, x2: float, y1: float, y2: float, x: float) -> float:
        """Линейная интерполяция между двумя точками"""
        if x2 == x1:
            return y1
        return y1 + (y2 - y1) * (x - x1) / (x2 - x1)

    @staticmethod
    def linear_spline(x_nodes: List[float], y_nodes: List[float], x: float) -> float:
        """Вычисление значения линейного сплайна"""
        if x <= x_nodes[0]:
            return y_nodes[0]
        if x >= x_nodes[-1]:
            return y_nodes[-1]

        for i in range(len(x_nodes) - 1):
            if x_nodes[i] <= x <= x_nodes[i + 1]:
                return ImageProcessor.linear_interpolation(
                    x_nodes[i], x_nodes[i + 1], y_nodes[i], y_nodes[i + 1], x
                )
        return y_nodes[-1]

    @staticmethod
    def bilinear_interpolation(x1: float, x2: float, y1: float, y2: float,
                               z11: float, z12: float, z21: float, z22: float,
                               x: float, y: float) -> float:
        """Билинейная интерполяция"""
        # Интерполяция по x в верхней и нижней строках
        z1 = ImageProcessor.linear_interpolation(x1, x2, z11, z21, x)
        z2 = ImageProcessor.linear_interpolation(x1, x2, z12, z22, x)
        # Интерполяция по y
        return ImageProcessor.linear_interpolation(y1, y2, z1, z2, y)

    # 1.4 Дискретное косинусное преобразование
    @staticmethod
    def dct_matrix(n: int) -> np.ndarray:
        """Создание матрицы ДКП размера n x n"""
        matrix = np.zeros((n, n))
        for i in range(n):
            for j in range(n):
                if i == 0:
                    alpha = np.sqrt(1/n)
                else:
                    alpha = np.sqrt(2/n)
                matrix[i, j] = alpha * np.cos((2*j + 1) * i * np.pi / (2 * n))
        return matrix

    @staticmethod
    def dct_2d(block: np.ndarray) -> np.ndarray:
        """2D ДКП с использованием матричного умножения"""
        n, m = block.shape
        dct_mat_n = ImageProcessor.dct_matrix(n)
        dct_mat_m = ImageProcessor.dct_matrix(m)
        return dct_mat_n @ block @ dct_mat_m.T

    @staticmethod
    def idct_2d(dct_block: np.ndarray) -> np.ndarray:
        """Обратное 2D ДКП"""
        n, m = dct_block.shape
        dct_mat_n = ImageProcessor.dct_matrix(n)
        dct_mat_m = ImageProcessor.dct_matrix(m)
        return dct_mat_n.T @ dct_block @ dct_mat_m

    @staticmethod
    def split_into_blocks(image_array: np.ndarray, block_size: int = 8) -> List[np.ndarray]:
        """Разбиение изображения на блоки"""
        height, width = image_array.shape[:2]
        blocks = []

        for i in range(0, height, block_size):
            for j in range(0, width, block_size):
                block = image_array[i:i+block_size, j:j+block_size]
                # Дополняем блок если нужно
                if block.shape[0] < block_size or block.shape[1] < block_size:
                    padded_block = np.zeros((block_size, block_size))
                    padded_block[:block.shape[0], :block.shape[1]] = block
                    blocks.append(padded_block)
                else:
                    blocks.append(block)

        return blocks

    @staticmethod
    def merge_blocks(blocks: List[np.ndarray], height: int, width: int, block_size: int = 8) -> np.ndarray:
        """Объединение блоков в изображение"""
        result = np.zeros((height, width))
        idx = 0

        for i in range(0, height, block_size):
            for j in range(0, width, block_size):
                block = blocks[idx]
                h_end = min(i + block_size, height)
                w_end = min(j + block_size, width)
                result[i:h_end, j:w_end] = block[:h_end-i, :w_end-j]
                idx += 1

        return result

    # Квантование
    @staticmethod
    def quantize(dct_coeffs: np.ndarray, quant_matrix: np.ndarray) -> np.ndarray:
        """Квантование коэффициентов ДКП"""
        return np.round(dct_coeffs / quant_matrix).astype(np.int32)

    @staticmethod
    def dequantize(quantized_coeffs: np.ndarray, quant_matrix: np.ndarray) -> np.ndarray:
        """Обратное квантование"""
        return quantized_coeffs * quant_matrix

    @staticmethod
    def get_luminance_quant_matrix(quality: int = 50) -> np.ndarray:
        """Стандартная матрица квантования для яркости (из ITU-T81)"""
        base_matrix = np.array([
            [16, 11, 10, 16, 24, 40, 51, 61],
            [12, 12, 14, 19, 26, 58, 60, 55],
            [14, 13, 16, 24, 40, 57, 69, 56],
            [14, 17, 22, 29, 51, 87, 80, 62],
            [18, 22, 37, 56, 68, 109, 103, 77],
            [24, 35, 55, 64, 81, 104, 113, 92],
            [49, 64, 78, 87, 103, 121, 120, 101],
            [72, 92, 95, 98, 112, 100, 103, 99]
        ])

        # Адаптация под качество
        if quality < 50:
            scale = 5000 / quality
        else:
            scale = 200 - 2 * quality

        scaled_matrix = np.ceil((base_matrix * scale) / 100)
        return np.clip(scaled_matrix, 1, 255).astype(np.int32)


