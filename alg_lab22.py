class JPEGEncoder:
    """Полный JPEG кодер/декодер"""

    def __init__(self):
        self.processor = ImageProcessor()
        self.compressor = JPEGCompressor()
        self.quality = 75

    def _convert_to_serializable(self, obj):
        """Рекурсивное преобразование numpy типов в стандартные Python типы"""
        if isinstance(obj, np.integer):
            return int(obj)
        elif isinstance(obj, np.floating):
            return float(obj)
        elif isinstance(obj, np.ndarray):
            return obj.tolist()
        elif isinstance(obj, dict):
            return {key: self._convert_to_serializable(value) for key, value in obj.items()}
        elif isinstance(obj, (list, tuple)):
            return [self._convert_to_serializable(item) for item in obj]
        else:
            return obj

    def _serialize_compressed_data(self, data: Dict) -> bytes:
        """Сериализация сжатых данных в байты"""
        import json
        # Конвертируем numpy типы в стандартные
        serializable_data = self._convert_to_serializable(data)
        json_str = json.dumps(serializable_data)
        return json_str.encode('utf-8')

    def _deserialize_compressed_data(self, compressed_bytes: bytes) -> Dict:
        """Десериализация сжатых данных"""
        import json
        data = json.loads(compressed_bytes.decode('utf-8'))
        # Преобразуем списки обратно в numpy массивы где нужно
        if 'quant_matrix' in data:
            data['quant_matrix'] = np.array(data['quant_matrix'])
        return data

    def compress_image(self, image: Image.Image, quality: int = 75,
                       use_ycbcr: bool = True) -> bytes:
        """Полное сжатие изображения"""
        self.quality = quality
        img_array = np.array(image)

        # Конвертация в YCbCr если нужно
        color_space = "RGB"
        if use_ycbcr and image.mode == 'RGB':
            img_array = self.processor.rgb_to_ycbcr(img_array)
            color_space = "YCbCr"

        # Получаем матрицу квантования
        quant_matrix = self.compressor.adjust_quantization_matrix(
            self.processor.get_luminance_quant_matrix(), quality
        )

        # Разбиваем на блоки и обрабатываем
        height, width = img_array.shape[:2]
        compressed_data = {
            'width': width,
            'height': height,
            'color_space': color_space,
            'quality': quality,
            'quant_matrix': quant_matrix.tolist(),
            'dc_coeffs': [],
            'ac_rle_codes': []
        }

        if len(img_array.shape) == 3:
            # Обрабатываем каждый канал
            for channel in range(img_array.shape[2]):
                channel_data = self._compress_channel(
                    img_array[:, :, channel], quant_matrix
                )
                compressed_data[f'dc_coeffs_ch{channel}'] = channel_data['dc_coeffs']
                compressed_data[f'ac_rle_codes_ch{channel}'] = channel_data['ac_rle_codes']
        else:
            channel_data = self._compress_channel(img_array, quant_matrix)
            compressed_data['dc_coeffs'] = channel_data['dc_coeffs']
            compressed_data['ac_rle_codes'] = channel_data['ac_rle_codes']

        # Сериализуем в байты
        return self._serialize_compressed_data(compressed_data)

    def _compress_channel(self, channel: np.ndarray, quant_matrix: np.ndarray) -> Dict:
      """Сжатие одного канала"""
      height, width = channel.shape
      # Смещаем значения в диапазон [-128, 127]
      channel = channel.astype(np.float32) - 128
      # Разбиваем на блоки
      blocks = self.processor.split_into_blocks(channel)
      all_dc_coeffs = []
      all_ac_rle_codes = []
      for block in blocks:
        # ДКП
        dct_coeffs = self.processor.dct_2d(block)

        # Квантование
        quantized = self.processor.quantize(dct_coeffs, quant_matrix)

        # Зигзаг обход
        zigzag = self.compressor.zigzag_traversal(quantized)

        # DC коэффициент
        dc = int(zigzag[0])  # Конвертируем в int
        ac = zigzag[1:]

        all_dc_coeffs.append(dc)

        # RLE для AC - конвертируем все в стандартные int
        rle_codes = self.compressor.rle_encode(ac)
        rle_codes = [(int(zero), int(val)) for zero, val in rle_codes]
        all_ac_rle_codes.append(rle_codes)

      # Разностное кодирование DC
      encoded_dc = self.compressor.differential_encode(all_dc_coeffs)

      return {
        'dc_coeffs': encoded_dc,
        'ac_rle_codes': all_ac_rle_codes
      }

    def _serialize_compressed_data(self, data: Dict) -> bytes:
        """Сериализация сжатых данных в байты"""
        import json
        json_str = json.dumps(data)
        return json_str.encode('utf-8')

    def _deserialize_compressed_data(self, compressed_bytes: bytes) -> Dict:
        """Десериализация сжатых данных"""
        import json
        return json.loads(compressed_bytes.decode('utf-8'))

    def decompress_image(self, compressed_bytes: bytes) -> Image.Image:
        """Полное восстановление изображения"""
        data = self._deserialize_compressed_data(compressed_bytes)

        width = data['width']
        height = data['height']
        color_space = data['color_space']
        quant_matrix = np.array(data['quant_matrix'])

        quant_matrix = self.compressor.adjust_quantization_matrix(
            self.processor.get_luminance_quant_matrix(), data['quality']
        )

        # Проверяем количество каналов
        if 'dc_coeffs_ch0' in data:
            # Цветное изображение
            channels = []
            for i in range(3):
                dc_coeffs = self.compressor.differential_decode(data[f'dc_coeffs_ch{i}'])
                ac_rle_codes = data[f'ac_rle_codes_ch{i}']

                channel = self._decompress_channel(
                    dc_coeffs, ac_rle_codes, height, width, quant_matrix
                )
                channels.append(channel)

            result = np.stack(channels, axis=-1)

            if color_space == "YCbCr":
                result = self.processor.ycbcr_to_rgb(result)
        else:
            # Черно-белое изображение
            dc_coeffs = self.compressor.differential_decode(data['dc_coeffs'])
            ac_rle_codes = data['ac_rle_codes']

            result = self._decompress_channel(
                dc_coeffs, ac_rle_codes, height, width, quant_matrix
            )

        result = np.clip(result, 0, 255).astype(np.uint8)

        if len(result.shape) == 3:
            return Image.fromarray(result, 'RGB')
        else:
            return Image.fromarray(result, 'L')

    def _decompress_channel(self, dc_coeffs: List[int], ac_rle_codes: List,
                            height: int, width: int, quant_matrix: np.ndarray) -> np.ndarray:
        """Декомпрессия одного канала"""
        blocks = []

        for dc, rle_codes in zip(dc_coeffs, ac_rle_codes):
            # Декодируем AC
            ac = self.compressor.rle_decode(rle_codes)

            # Собираем коэффициенты
            zigzag = [dc] + ac

            # Обратный зигзаг
            quantized = self.compressor.inverse_zigzag_traversal(zigzag, (8, 8))

            # Обратное квантование
            dct_coeffs = self.processor.dequantize(quantized, quant_matrix)

            # Обратное ДКП
            block = self.processor.idct_2d(dct_coeffs)

            # Смещаем обратно
            block = block + 128

            blocks.append(block)

        # Объединяем блоки
        return self.processor.merge_blocks(blocks, height, width)


