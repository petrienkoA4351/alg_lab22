def test_color_space_conversion():
    """Тестирование конвертации цветовых пространств"""
    processor = ImageProcessor()

    # Создаем тестовое изображение с известными цветами
    test_img = Image.new('RGB', (100, 100))
    pixels = test_img.load()

    # Заполняем тестовыми цветами
    colors = [(255, 0, 0), (0, 255, 0), (0, 0, 255), (255, 255, 0), (255, 0, 255), (0, 255, 255)]
    for i, color in enumerate(colors):
        for x in range(20):
            for y in range(i*15, (i+1)*15):
                if x < 100 and y < 100:
                    pixels[x, y] = color

    # Конвертируем RGB -> YCbCr -> RGB
    rgb_array = np.array(test_img)
    ycbcr_array = processor.rgb_to_ycbcr(rgb_array)
    rgb_reconstructed = processor.ycbcr_to_rgb(ycbcr_array)

    # Вычисляем ошибку
    error = np.abs(rgb_array.astype(np.float32) - rgb_reconstructed.astype(np.float32))
    mse = np.mean(error ** 2)
    psnr = 10 * np.log10(255**2 / mse) if mse > 0 else float('inf')

    print(f"\n=== Тестирование цветовых пространств ===")
    print(f"MSE: {mse:.2f}")
    print(f"PSNR: {psnr:.2f} dB")

    if psnr > 40:
        print("✓ Преобразования работают корректно, видимые артефакты отсутствуют")

    return test_img, Image.fromarray(rgb_reconstructed, 'RGB')

